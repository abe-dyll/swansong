export function ageColor(age) {
  if (age >= 80) return '#A63A2B';
  if (age >= 72) return '#B9860B';
  return '#5C7A29';
}

export function maxAge(artist) {
  return Math.max(...artist.members.map((m) => computeAge(m.birthYear, m.deathYear)));
}

export function artistHasPassed(artist) {
  return artist.members.every((m) => hasPassed(m.deathYear));
}
